    void OpenMenuBar(){
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i=0; i<widthmenu; i++){
                    Menu.setSize(widthmenu,heightmenu);
                }
            }
        }).start();
    }
    
    void CloseMenuBar(){
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i=widthmenu; i>0; i--){
                    Menu.setSize(i,heightmenu);
                }
            }
        }).start();
    }

